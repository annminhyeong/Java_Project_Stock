package poly.dto;

public class NewsDTO {
	
	private String news_link;
	private String news_title;
	private String news_contents;
	private String news_summary;
	private String news_reporter;
	private String news_regdate;
	
	private String stock_name;
	private String stock_code;
	

	
	
	public String getNews_link() {
		return news_link;
	}
	public void setNews_link(String news_link) {
		this.news_link = news_link;
	}
	public String getNews_title() {
		return news_title;
	}
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	public String getNews_contents() {
		return news_contents;
	}
	public void setNews_contents(String news_contents) {
		this.news_contents = news_contents;
	}
	public String getNews_summary() {
		return news_summary;
	}
	public void setNews_summary(String news_summary) {
		this.news_summary = news_summary;
	}
	public String getNews_reporter() {
		return news_reporter;
	}
	public void setNews_reporter(String news_reporter) {
		this.news_reporter = news_reporter;
	}
	public String getNews_regdate() {
		return news_regdate;
	}
	public void setNews_regdate(String news_regdate) {
		this.news_regdate = news_regdate;
	}
	public String getStock_name() {
		return stock_name;
	}
	public void setStock_name(String stock_name) {
		this.stock_name = stock_name;
	}
	public String getStock_code() {
		return stock_code;
	}
	public void setStock_code(String stock_code) {
		this.stock_code = stock_code;
	}
	
	
	
}
